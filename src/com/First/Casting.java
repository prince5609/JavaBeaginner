package com.First;

public class Casting {
    public static void main(String[] args) {
        double price = 100.00;
        double final_price = price + 18.00;
        System.out.println(final_price);

        int price1 = 100;
        int fp = price1 + (int)18.99;
        System.out.println(fp);

    }
}
